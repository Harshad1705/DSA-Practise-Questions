class Pre_and_Post_Increment {

    public static void main(String[] args) {
        
        int i = 7 ;

        System.out.println(++i);  // i = 8     print=8
        System.out.println(i++);  // print = 8    i = 9
        System.out.println(--i);  // i = 8     print= 8
        System.out.println(i--);  // print = 8   i = 7



    }



}
